package com.liaotao.jackson.config;

public class User {

    public String uid;
    public String name;
    public int age;

    public String getUid() {
        return uid;
    }
    public void setUid(String uid) {
        this.uid = uid;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public int getAge() {
        return age;
    }
    public void setAge(short age) {
        this.age = age;
    }
}
